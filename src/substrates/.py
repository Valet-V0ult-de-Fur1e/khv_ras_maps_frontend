import rasterio

def read_geotiff_info(file_path):
    # Открываем файл с помощью rasterio
    with rasterio.open(file_path) as src:
        # Получаем название файла
        file_name = src.name
        
        # Получаем размеры изображения (ширина и высота)
        width = src.width
        height = src.height
        
        # Получаем координаты крайних точек (в проекционных координатах)
        bounds = src.bounds
        minx, miny, maxx, maxy = bounds
        
        # Выводим информацию
        print(f"Название файла: {file_name}")
        print(f"Размеры: {width} x {height}")
        
        # Координаты в первом формате (minx, miny) - нижний левый угол и (maxx, maxy) - верхний правый угол
        print("Координаты крайних точек в проекционных координатах:")
        print(f"Нижний левый угол: ({minx}, {miny})")
        print(f"Верхний правый угол: ({maxx}, {maxy})")
        
        # Координаты в другом формате, например, в географических координатах (широта и долгота),
        # если у нас есть возможность преобразовать их, используя привязку проекции (например, WGS84).
        # Для этого можно использовать pyproj, если требуется.
        # Но для простоты, оставим их как есть.
        print("Координаты крайних точек в проекционных координатах:")
        print(f"Нижний левый угол: ({minx}, {miny})")
        print(f"Верхний правый угол: ({maxx}, {maxy})")

# Пример использования:
file_path = 'test.tif'
read_geotiff_info(file_path)
